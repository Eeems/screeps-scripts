export function memcmp(vl: number, vr: number, n: number): number{
    vl;vr;n;
    return 0;
}
export function memcpy(dest: number, src: number, n: number): number{
    dest;src;n;
    return 0;
}
export function memset(dest: number, c: number, n: number): number{
    dest;c;n;
    return 0;
}
export function malloc(size: number): number{
    size;
    return 0;
}
export function realloc(ptr: number, size: number): number{
    ptr;size;
    return 0;
}
export function free(ptr: number): void{
    ptr;
}
